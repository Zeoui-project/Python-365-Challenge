# Python program to convert decimal into other number systems
dec = int(input("Enter a Decimal Number: "))

# Decimal to binary
print(bin(dec), "in Binary.")

# Decimal to octal
print(oct(dec), "in Octal.")

# Decimal to Hexadecimal
print(hex(dec), "in Hexadecimal.")


